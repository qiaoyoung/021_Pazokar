Byte * ViewDataToCache(Byte *data) {
    int fromVoice = data[0];
    int size = data[1];
    Byte childGreen = data[2];
    int cornerMin = data[3];
    if (!fromVoice) return data + cornerMin;
    for (int i = cornerMin; i < cornerMin + size; i++) {
        int value = data[i] + childGreen;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[cornerMin + size] = 0;
    return data + cornerMin;
}

NSString *StringFromViewData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)ViewDataToCache(data)];
}
