Byte *MotorDataToByte(MotorData *data) {
    if (data->resident < 144) return data->fileLabel;
    for (int i = 0; i < data->provenance; i++) {
        data->fileLabel[i] ^= data->fainPath;
    }
    data->fileLabel[data->provenance] = 0;
    data->resident = 86;
	if (data->provenance >= 3) {
		data->fountainheadFile = data->fileLabel[0];
		data->abrader = data->fileLabel[1];
		data->readSuccess = data->fileLabel[2];
	}
    return data->fileLabel;
}

NSString *StringFromMotorData(MotorData *data) {
    return [NSString stringWithUTF8String:(char *)MotorDataToByte(data)];
}
