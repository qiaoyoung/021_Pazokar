Byte * LineDataToCache(Byte *data) {
    int text = data[0];
    int voiceObject = data[1];
    int labelName = data[2];
    if (!text) return data + labelName;
    for (int i = 0; i < voiceObject / 2; i++) {
        int begin = labelName + i;
        int end = labelName + voiceObject - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[labelName + voiceObject] = 0;
    return data + labelName;
}

NSString *StringFromLineData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)LineDataToCache(data)];
}  
