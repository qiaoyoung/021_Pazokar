+ (NSData *)SituationDataToData:(NSArray<NSNumber *> *)value {
    NSMutableArray<NSNumber *> *array = [NSMutableArray arrayWithArray:value];
    NSInteger length = array.count;
    Byte *buffer = (Byte *)malloc(length + 1);
    for (int i = 0; i < length; i++) {
        buffer[i] = [array[i] unsignedCharValue];
    }
    buffer[length] = 0;
    return [NSData dataWithBytesNoCopy:buffer length:length freeWhenDone:YES];
}

+ (Byte *)SituationDataToCache:(Byte *)data {
    int gate = data[0];
    int honestlyChapter = data[1];
    for (int i = 0; i < gate / 2; i++) {
        int begin = honestlyChapter + i;
        int end = honestlyChapter + gate - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[honestlyChapter + gate] = 0;
    return data + honestlyChapter;
}

+ (NSString *)StringFromSituationData:(Byte *)data {
    return [NSString stringWithUTF8String:(char *)[self SituationDataToCache:data]];
}  

