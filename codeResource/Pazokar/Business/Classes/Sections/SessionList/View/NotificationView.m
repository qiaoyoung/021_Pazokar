+ (Byte *)OnDataToCache:(Byte *)data {
    int messageCell = data[0];
    Byte infoCell = data[1];
    int verticalInputInfo = data[2];
    for (int i = verticalInputInfo; i < verticalInputInfo + messageCell; i++) {
        int value = data[i] + infoCell;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[verticalInputInfo + messageCell] = 0;
    return data + verticalInputInfo;
}

+ (NSString *)StringFromOnData:(Byte *)data {
    return [NSString stringWithUTF8String:(char *)[self OnDataToCache:data]];
}

